"""Шаблоны промптов для генерации гипотез.

Ключевая идея интерпретируемости: модель обязана
  1) ссылаться на конкретные источники по их ID ([S1], [S2] ...);
  2) выставлять оценки по 4 названным осям и обосновывать КАЖДУЮ.
Итоговый ранг считается вне модели прозрачной формулой (см. models.composite_score).
"""

SYSTEM_PROMPT = (
    "Ты — старший научный стратег НИОКР в области материаловедения, металлургии, "
    "катализа и электрохимии. Ты помогаешь исследовательским группам формулировать "
    "проверяемые научные гипотезы для достижения заданного целевого показателя (KPI). "
    "Ты рассуждаешь строго и опираешься на предоставленную базу знаний. "
    "Ты не выдумываешь ссылки и не преувеличиваешь новизну. "
    "Отвечай ИСКЛЮЧИТЕЛЬНО валидным JSON без пояснений вне JSON."
)

# Схема, которую модель обязана вернуть. Держим рядом с промптом.
_SCHEMA = """
Верни JSON строго такого вида:
{
  "hypotheses": [
    {
      "statement": "Чёткая проверяемая формулировка: ЧТО изменить и КАК это повлияет на KPI",
      "rationale": "Научное обоснование со ссылками на источники по ID, напр. [S1], [S3]",
      "mechanism": "Предполагаемый физико-химический механизм (кратко, по сути)",
      "validation": "Конкретный эксперимент/метод проверки: образцы, условия, что измеряем",
      "evidence": [
        {"source_id": "S1", "snippet": "короткая цитата/факт из источника", "relevance": "чем помогает гипотезе"}
      ],
      "tags": ["ключевое-слово", "..."],
      "novelty": 0-100,
      "novelty_rationale": "почему такая оценка новизны (1-2 предложения)",
      "value": 0-100,
      "value_rationale": "почему такая потенциальная ценность для KPI",
      "feasibility": 0-100,
      "feasibility_rationale": "насколько реально проверить имеющимися средствами",
      "risk": 0-100,
      "risk_rationale": "ключевые научные/технические риски и неопределённости"
    }
  ]
}
Шкалы: novelty/value/feasibility/risk — целые 0..100.
  novelty  — насколько идея нетривиальна относительно базы знаний (100 = прорыв).
  value    — ожидаемый вклад в достижение KPI (100 = решающий).
  feasibility — реализуемость проверки имеющимися ресурсами (100 = легко проверить).
  risk     — суммарный научно-технический риск/неопределённость (100 = очень высокий).
"""


def build_generation_prompt(project, retrieved, n: int):
    """project: Project; retrieved: list[{source, score, terms}]; n: сколько гипотез."""
    direction = "увеличить" if (project.kpi_direction or "increase") == "increase" else "снизить"

    kb_lines = []
    id_map = {}   # "S1" -> реальный source.id
    for i, item in enumerate(retrieved, start=1):
        s = item["source"]
        sid = f"S{i}"
        id_map[sid] = s.id
        meta = " · ".join(filter(None, [
            {"literature": "литература", "report": "отчёт", "experiment": "эксперимент"}
            .get(s.source_type, s.source_type),
            str(s.year) if s.year else None,
            s.authors or None,
        ]))
        terms = ", ".join(item.get("terms") or [])
        excerpt = (s.content or "")[:900]
        kb_lines.append(
            f"[{sid}] {s.title} ({meta})\n"
            f"    релевантность={item['score']} совпавшие термины: {terms}\n"
            f"    {excerpt}"
        )
    kb_block = "\n\n".join(kb_lines) if kb_lines else "(база знаний пуста — опирайся на общие принципы, но помечай гипотезы высоким риском)"

    user = f"""ЦЕЛЕВОЙ ПОКАЗАТЕЛЬ (KPI)
Проект: {project.title}
Цель: {project.kpi_target}
Метрика: {project.kpi_metric or "не задана"} (направление: {direction})
Область: {project.domain or "материаловедение"}
Ограничения/ресурсы: {project.constraints or "не заданы"}

БАЗА ЗНАНИЙ (отобрана автоматически по TF-IDF под данный KPI)
{kb_block}

ЗАДАЧА
Сгенерируй {n} РАЗНООБРАЗНЫХ, взаимодополняющих и проверяемых научных гипотез,
направленных на достижение KPI. Требования:
  • опирайся на конкретные источники и ссылайся на них по ID ([S1], [S2] ...);
  • каждая гипотеза должна предлагать иной механизм/подход (не переформулировки одной идеи);
  • формулировка должна быть проверяемой в эксперименте;
  • оценки калибруй честно и обосновывай каждую;
  • если источников недостаточно — это должно отражаться в снижении feasibility и росте risk.

{_SCHEMA}
"""
    return SYSTEM_PROMPT, user, id_map
