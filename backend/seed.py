def seed_if_empty():
    if Project.query.first() is not None:
        return False
    project = Project(**DEMO_PROJECT)
    db.session.add(project)
    db.session.flush()
    for s in DEMO_SOURCES:
        db.session.add(KnowledgeSource(project_id=project.id, **s))
    db.session.commit()
    return True
